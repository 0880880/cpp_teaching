
// if , else , else if
#include <iostream>
using namespace std;

int main() {
    int day;

    cout << "Please enter the day number: ";
    cin >> day;

    if (day == 1) {
        cout << "shanbe" << endl;
    }
    else if (day == 2) {
        cout << "1shanbe" << endl;
    }
    else if (day == 3) {
        cout << "2shanbe" << endl;
    }
    else if (day == 4) {
        cout << "3shanbe" << endl;
    }
    else if (day == 5) {
        cout << "4shanbe" << endl;
    }
    else if (day == 6) {
        cout << "5shanbe" << endl;
    }
    else if (day == 7) {
        cout << "jome" << endl;
    }
    else {
        cout << "try again" << endl;
    }

    return 0;
}





















